# 3.0 List（列表）
# 加号 + 是列表连接运算符，星号 * 是重复操作
list = [ 'runoob', 786 , 2.23, 'john', 70.2 ]
tinyList=[123, 'john']
print(list[0])           # 输出列表的第一个元素
print(list[1:3])         # 输出第二个至第三个元素
print(list[2:])          # 输出从第三个开始至列表末尾的所有元素
print(list + tinyList)   # 打印组合的列表